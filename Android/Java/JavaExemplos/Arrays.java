public class Arrays {
    public static void main(String[] args) {

        String[] nomes = { "Lucas", "Eduardo", "Barbosa", "de", "Siqueira" };
        int[] numeros = { 1, 2, 3, 4, 5 };

        for (int i = 0; i < 5; i++) {
            System.out.println(nomes[i]);
        }

        System.out.println(nomes[0]);
        System.out.println(numeros[4]);
    }
}
